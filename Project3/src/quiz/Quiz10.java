package quiz;

public class Quiz10 {

	public static void main(String[] args) {
		
		// 코드를 보고 풀이과정과 예상 결과를 주석으로 작성하세요.
		
		int result1 = -5 + 3 * 10 / 2; // (3 * 10 / 2)를 먼저 계산 -> 30/2=15. 그 후에 -5를 하면 10
		System.out.println(result1);
		
		
		int x = 1;
		
		int result2 = (++x) + 5; // 단항연산자를 먼저 계산하고(2) 5를 더해서 7
		System.out.println(result2);
		
	}

}
