package main;

public class Ex1 {

	public static void main(String[] args) {
		
	// 자료형 + 변수의 이름
		int x; // 정수형 변수 선언
		x = 10; // 변수에 값 10을 대입 (오른쪽에 있는 값을 왼쪽 변수에 넣는 식)
		
		System.out.println(x); // 변수 안에 담긴 값이 출력
		
		// "sysout" 작성 > ctrl + space > 프린트 함수가 자동으로 완성됨
		
		System.out.println("안녕하세요"); // println 함수는 출력 후 줄바꿈이 실행됨
		System.out.print("반갑습니다"); // print 함수는 줄바꿈 X
		System.out.print("반갑습니다");

	}

}
